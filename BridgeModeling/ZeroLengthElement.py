def get_soil_depth(mat_tag):
    """
    Returns the corresponding depth for mat_tag in 101..130 or 201..230.
    Returns None if mat_tag not in those soil ranges (i.e., if it’s not “scourable”).
    """
    if 101 <= mat_tag <= 130:
        return depths[mat_tag - 101]
    elif 201 <= mat_tag <= 230:
        return depths[mat_tag - 201]
    else:
        return None


